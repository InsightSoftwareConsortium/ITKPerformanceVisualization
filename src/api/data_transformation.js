var findValue = function(object, dir) {
  let keys = dir.split(".");
  let pointer = object;

  for (let i = 0; i < keys.length; i++) {
    if (Array.isArray(pointer)){
      let sum = [];
      for (let elem in pointer)
        sum = sum.concat(pointer[elem][keys[i]]);
      pointer = sum;
    }
    else if (pointer[keys[i]] === undefined) return undefined;
    else pointer = pointer[keys[i]];
  }
  return pointer;
};

